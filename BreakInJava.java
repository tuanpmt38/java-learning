public class BreakInJava{
    public static void main(String[] args) {
        int i=1;
        switch(i){
            case 1:
            System.out.println("gia tri i = 1");
            break ;
            case 5:
            System.out.println("gia tri i = 5");
            break ;
            default:
            System.out.println("gia tri i khac 1 va 5");
        }
    }
}